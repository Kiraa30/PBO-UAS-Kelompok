    private void buttonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSubmitActionPerformed
        // TODO add your handling code here:                                         
    // Validasi input: Pastikan semua field diisi
    if (txtIdOrder.getText().isEmpty() ||
        txtNama.getText().isEmpty() ||
        txtNoTelepon.getText().isEmpty() ||
        txtAlamat.getText().isEmpty() ||
        jcomboboxLayanan.getSelectedIndex() == 0 ||
        jcomboboxMetode.getSelectedIndex() == 0 ||
        txtHargaTotal.getText().isEmpty()) {

        JOptionPane.showMessageDialog(this, "Harap isi semua field dengan benar!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        // Query SQL untuk menyimpan data ke tabel prosespesanan
        String sql = "INSERT INTO prosespesanan (order_id, tanggalmasuk, nama_customer, no_telepon, alamat, layanan, metode, hargatotal) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
        java.sql.Connection conn = KoneksiDB.konek();
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);

        // Mengisi parameter query
        pst.setInt(1, Integer.parseInt(txtIdOrder.getText())); // Mengisi order_id
        pst.setTimestamp(2, new java.sql.Timestamp(System.currentTimeMillis())); // Mengisi tanggalmasuk dengan waktu saat ini
        pst.setString(3, txtNama.getText()); // Mengisi nama_customer
        pst.setString(4, txtNoTelepon.getText()); // Mengisi no_telepon
        pst.setString(5, txtAlamat.getText()); // Mengisi alamat
        pst.setString(6, jcomboboxLayanan.getSelectedItem().toString()); // Mengisi layanan
        pst.setString(7, jcomboboxMetode.getSelectedItem().toString()); // Mengisi metode
        pst.setDouble(8, Double.parseDouble(txtHargaTotal.getText())); // Mengisi hargatotal

        // Eksekusi query
        pst.execute();
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan ke database!");

        // Tambahkan data ke JTable
        DefaultTableModel model = (DefaultTableModel) tabelProsesPesanan.getModel();
        model.addRow(new Object[]{
            txtIdOrder.getText(),
            new java.sql.Timestamp(System.currentTimeMillis()),
            txtNama.getText(),
            txtNoTelepon.getText(),
            txtAlamat.getText(),
            jcomboboxLayanan.getSelectedItem().toString(),
            jcomboboxMetode.getSelectedItem().toString(),
            txtHargaTotal.getText()
        });

        // Kosongkan input setelah data ditambahkan
        txtIdOrder.setText("");
        txtNama.setText("");
        txtNoTelepon.setText("");
        txtAlamat.setText("");
        jcomboboxLayanan.setSelectedIndex(0);
        jcomboboxMetode.setSelectedIndex(0);
        txtHargaTotal.setText("");

    } catch (SQLException | HeadlessException e) {
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
    } catch (ClassNotFoundException ex) {
        Logger.getLogger(OrderDetail.class.getName()).log(Level.SEVERE, null, ex);
    }

    // Memuat ulang data pada tabel
    loadDataProsesPesanan();

    }//GEN-LAST:event_buttonSubmitActionPerformed

