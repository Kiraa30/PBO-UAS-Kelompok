    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        try {
        // Ambil nilai dari input
        String layanan = jcomboboxLayanan.getSelectedItem().toString();
        String metode = jcomboboxMetode.getSelectedItem().toString();
        double berat = Double.parseDouble(txtBerat.getText());

        // Harga dasar
        double hargaDasar = 6000; // Contoh: harga per Kg adalah 6000
        double biayaLayanan = 0;
        double biayaMetode = 0;

        // Hitung biaya tambahan untuk layanan antar/jemput
        if (layanan.equalsIgnoreCase("Motor")) {
            biayaLayanan = 2000; // Biaya tambahan untuk layanan antar/jemput
        } else if (layanan.equalsIgnoreCase("Mobil")) {
            biayaLayanan = 10000; // Biaya tambahan untuk layanan antar/jemput
        }
        
        // Hitung biaya tambahan berdasarkan metode cuci
        if (metode.equalsIgnoreCase("Cuci Kering")) {
            biayaMetode = 5000; // Biaya tambahan untuk metode kilat
        } else if (metode.equalsIgnoreCase("Cuci Kering + Setrika")) {
            biayaMetode = 7000; // Biaya tambahan untuk metode premium
        }

        // Hitung total harga
        double hargaTotal = (hargaDasar + biayaMetode) * berat + biayaLayanan;

        // Tampilkan hasil perhitungan ke txtHargaTotal
        txtHargaTotal.setText(String.valueOf(hargaTotal));
    } catch (NumberFormatException e) {
        // Jika input berat tidak valid
        JOptionPane.showMessageDialog(this, "Masukkan berat yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }   
    }//GEN-LAST:event_btnHitungActionPerformed

