    public void loadDataUser() {
    // Nama kolom di JTable
   String[] kolom = {"ID Customer", "Nama", "No Telepon", "Alamat", "Layanan"};
        DefaultTableModel model = new DefaultTableModel(null, kolom);

        try {
            // Gunakan metode konek() dari KoneksiDB
            Connection conn = KoneksiDB.konek();
            Statement stmt = conn.createStatement();

            // Query untuk mengambil data dari tabel
            String sql = "SELECT * FROM menupesanan";
            ResultSet rs = stmt.executeQuery(sql);

            // Iterasi data dari ResultSet ke JTable
            while (rs.next()) {
                String idCustomer = rs.getString("id_customer");
                String nama = rs.getString("nama");
                String noTelepon = rs.getString("no_telepon");
                String alamat = rs.getString("alamat");
                String layanan = rs.getString("layanan");

                // Tambahkan data ke model
                String[] data = {idCustomer, nama, noTelepon, alamat, layanan};
                model.addRow(data);
            }

            // Set model ke JTable
            tabelRincian.setModel(model);

        } catch (ClassNotFoundException | SQLException e) {
            System.out.println("Error: " + e.getMessage());
        }
    }
 
